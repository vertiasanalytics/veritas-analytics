export function getFallbackIndexes(
  indexType: string,
  startPeriod: string,
  endPeriod: string
): FallbackIndexEntry[] {
  let dataMap: Record<string, number>;

  switch (indexType) {
    case "IPCA":
    case "IPCA_E":
      dataMap = IPCA_FALLBACK;
      break;
    case "INPC":
      dataMap = INPC_FALLBACK;
      break;
    case "SELIC":
      dataMap = SELIC_FALLBACK;
      break;
    default:
      dataMap = IPCA_FALLBACK;
  }

  return Object.entries(dataMap)
    .filter(([period]) => period >= startPeriod && period <= endPeriod)
    .map(([period, rate]) => ({
      period,
      rate,
      source: `Fallback Dev - ${indexType} (NÃO USAR EM PRODUÇÃO)`,
    }))
    .sort((a, b) => a.period.localeCompare(b.period));
}
